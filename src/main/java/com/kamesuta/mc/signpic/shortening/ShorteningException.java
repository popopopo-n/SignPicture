package com.kamesuta.mc.signpic.shortening;

public class ShorteningException extends Exception {

	public ShorteningException() {
		super();
	}

	public ShorteningException(final String message) {
		super(message);
	}

	public ShorteningException(final String message, final Throwable cause) {
		super(message, cause);
	}
}
